package mmud.database.entities.web;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;

/**
 *
 * @author maartenl
 */
@Entity
@Table(name = "familyvalues", catalog = "mmud", schema = "")
@NamedQueries(
{
    @NamedQuery(name = "FamilyValue.findAll", query = "SELECT f FROM FamilyValue f"),
    @NamedQuery(name = "FamilyValue.findById", query = "SELECT f FROM FamilyValue f WHERE f.id = :id"),
    @NamedQuery(name = "FamilyValue.findByDescription", query = "SELECT f FROM FamilyValue f WHERE f.description = :description")
})
public class FamilyValue implements Serializable
{
    private static final long serialVersionUID = 1L;
    @Id
    @Basic(optional = false)
    @NotNull
    @Column(name = "id")
    private Integer id;
    @Size(max = 40)
    @Column(name = "description")
    private String description;

    public FamilyValue()
    {
    }

    public FamilyValue(Integer id)
    {
        this.id = id;
    }

    public Integer getId()
    {
        return id;
    }

    public void setId(Integer id)
    {
        this.id = id;
    }

    public String getDescription()
    {
        return description;
    }

    public void setDescription(String description)
    {
        this.description = description;
    }

    @Override
    public int hashCode()
    {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object)
    {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof FamilyValue))
        {
            return false;
        }
        FamilyValue other = (FamilyValue) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id)))
        {
            return false;
        }
        return true;
    }

    @Override
    public String toString()
    {
        return "mmud.database.entities.web.FamilyValue[ id=" + id + " ]";
    }

}
